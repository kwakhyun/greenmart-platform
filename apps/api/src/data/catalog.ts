function createProduct(
  overrides: Partial<Product> &
    Pick<Product, "id" | "name" | "brand" | "category">,
): Product {
  const originalPrice = overrides.originalPrice ?? 25000;
  const salePrice = overrides.salePrice ?? originalPrice;
  const discountRate =
    originalPrice > salePrice
      ? Math.round((1 - salePrice / originalPrice) * 100)
      : 0;

  return {
    slug: overrides.name.replace(/\s+/g, "-").toLowerCase(),
    description: `${overrides.brand.name}의 ${overrides.name} 상세 설명입니다.`,
    shortDescription: `${overrides.brand.name} ${overrides.name}`,
    originalPrice,
    salePrice,
    images: [
      {
        id: `img-${overrides.id}-1`,
        url: "https://images.unsplash.com/photo-1556228578-0d85b1a4d571?w=400&h=400&fit=crop",
        alt: overrides.name,
        sortOrder: 1,
        type: "main",
      },
    ],
    options: [],
    tags: [] as ProductTag[],
    salesChannels: ["ONLINE", "OFFLINE"] as SalesChannel[],
    status: "ACTIVE",
    reviewSummary: {
      averageRating: 4.2 + Math.random() * 0.7,
      totalCount: Math.floor(Math.random() * 5000) + 100,
      distribution: { 5: 60, 4: 20, 3: 10, 2: 5, 1: 5 },
    },
    createdAt: "2025-01-15T09:00:00Z",
    updatedAt: "2026-02-20T14:30:00Z",
    ...overrides,
    discountRate:
      overrides.discountRate !== undefined
        ? overrides.discountRate
        : discountRate,
  } as Product;
}

